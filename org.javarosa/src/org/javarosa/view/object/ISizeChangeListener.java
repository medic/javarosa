package org.javarosa.view.object;

/**
 * The SizeChangeListener interface is implemented to allow
 * the receipt of Size Change events.
 * 
 * @author ctsims
 * @date Aug-09-2007
 *
 */
public interface ISizeChangeListener {
	/**
	 * Called when an size change has occured.
	 */
	public void sizeChanged();
}
